function highlightButtons(symbol) {
    let buttons_ids = buttons_dict[symbol];
    if (buttons_ids !== undefined) {
        for (let i = 0; i < buttons_ids.length; i++) {
            let button = document.getElementById(buttons_ids[i]);
            button.style.background = "#FFFFCC";
            button.style.color = "#212121";
            button.style.border = "2px solid #212121";
        }
    }
    let rus_buttons_ids = rus_buttons_dict[symbol];
    if (rus_buttons_ids !== undefined) {
        for (let i = 0; i < rus_buttons_ids.length; i++) {
            let button = document.getElementById(rus_buttons_ids[i]);
            button.style.background = "#FFFFCC";
            button.style.color = "#212121";
            button.style.border = "2px solid #212121";
        }
    }
}

function unHighlightButtons(symbol) {
    let buttons_ids = buttons_dict[symbol];
    if (buttons_ids !== undefined) {
        for (let i = 0; i < buttons_ids.length; i++) {
            let button = document.getElementById(buttons_ids[i]);
            button.style.background = "#212121";
            button.style.color = "white";
            button.style.border = "2px solid lightgrey";
        }
    }
    let rus_buttons_ids = rus_buttons_dict[symbol];
    if (rus_buttons_ids !== undefined) {
        for (let i = 0; i < rus_buttons_ids.length; i++) {
            let button = document.getElementById(rus_buttons_ids[i]);
            button.style.background = "#212121";
            button.style.color = "white";
            button.style.border = "2px solid lightgrey";
        }
    }
}
